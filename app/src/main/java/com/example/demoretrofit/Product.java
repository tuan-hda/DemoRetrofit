package com.example.demoretrofit;

public class Product {
    private int id;
    private double price;
    private String title, image;

    public int getId() {
        return id;
    }

    public double getPrice() {
        return price;
    }

    public String getTitle() {
        return title;
    }

    public String getImage() {
        return image;
    }
}
